let number = 1;

console.log(number++); // 1 (постинкремент, сначала выводится 1, потом number увеличивается на 1)
console.log(++number); // 3 (прединкремент, сначала number становится 3, затем выводится 3)
console.log(number--); // 3 (постинкремент, сначала выводится 3, потом number увеличивается на 1)
console.log(--number); // 1 (прединкремент, number уменьшается до 1, затем выводится 1)